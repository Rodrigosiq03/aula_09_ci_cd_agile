def area_of_rectangle(width, height):
    area = width*height
    return area

def perimeter_of_rectangle(width, height):
    perimeter = 2 * (width + height)
    return perimeter

def sub(value1, value2):
    sub = value1 - value2
    return sub

def add(value1, value2):
    add = value1 + value2
    return add

def div(value1, value2):
    div = value1 / value2
    return div

def mult(value1, value2):
    mult = value1 * value2
    return mult